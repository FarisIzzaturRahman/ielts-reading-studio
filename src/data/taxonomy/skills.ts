export const READING_SKILL_IDS = READING_SKILLS.map((item) => item.id);

export function getSkillBySlug(slug: string) {
  return READING_SKILLS.find((item) => item.slug === slug);
}

export function getSkillById(id: SkillTag | string) {
  return READING_SKILLS.find((item) => item.id === id);
}

export function skillSlug(skill: SkillTag | string) {
  return getSkillById(skill)?.slug ?? skill.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}
