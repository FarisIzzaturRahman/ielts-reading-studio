function makeTest(blueprint: TopicBlueprint): ReadingTest {
  const testWithoutMetadata: Omit<ReadingTest, "metadata"> = {
    testId: blueprint.testId,
    title: blueprint.title,
    description: `${blueprint.topic} mini simulation for IELTS Academic Reading self-study.`,
    topic: blueprint.topic,
    difficulty: blueprint.difficulty,
    targetBand: blueprint.targetBand,
    mode: "mini",
    testType: "Academic",
    timeLimitMinutes: blueprint.timeLimitMinutes,
    estimatedTimeMinutes: blueprint.timeLimitMinutes,
    totalQuestions: 20,
    passages: makePassages(blueprint),
    questions: makeQuestions(blueprint),
  };

  return {
    ...testWithoutMetadata,
    metadata: buildTestMetadata(testWithoutMetadata),
  };
}

export const readingTests: ReadingTest[] = BLUEPRINTS.map(makeTest);

export function getTestById(testId: string): ReadingTest | undefined {
  return readingTests.find((test) => test.testId === testId);
}

export function getRecommendedNextTest(currentTestId: string, score: number) {
  const currentIndex = readingTests.findIndex((test) => test.testId === currentTestId);
  if (score >= 16) {
    return readingTests[Math.min(currentIndex + 2, readingTests.length - 1)];
  }
  if (score >= 12) {
    return readingTests[Math.min(currentIndex + 1, readingTests.length - 1)];
  }
  return readingTests[Math.max(currentIndex - 1, 0)];
}
